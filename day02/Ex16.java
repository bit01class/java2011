// 증감연산자
class Ex16{

  public static void main(String[] args)
  {
    int a;
    a=1;
    System.out.println("a="+a);
    a=a+1;
    System.out.println("a="+a);
    a+=1;		// a=a+1;
    System.out.println("a="+a);
    a++; // a++, a--, ++a, --a
    System.out.println("a="+a);
    //System.out.println(+(+a));
    System.out.println(++a);
    System.out.println(a);
  }

}



